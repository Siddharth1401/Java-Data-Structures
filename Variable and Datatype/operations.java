// Classic Operations in java
public class operations {
    public static void main(String[] args){
        int x, y, z;
        x = y = z = 2;
        x += y;
        System.out.print(x);
        System.out.print(y);
        System.out.print(z);
        y -= z;
        System.out.print(x);
        System.out.print(y);
        System.out.print(z);
        z /= (x + y);
        System.out.print(x);
        System.out.print(y);
        System.out.print(z);
        }}